        {
            foreach ($attributes as $attr => $val)
            {
                if ($priority == 'tag')
                    $attributes_data[$attr] = $val;
                else
                    $result['attr'][$attr] = $val; //Set all the attributes in a array called 'attr'
            }
        }
        if(is_array($result) && count($result) == 0) {
          $result = "";
        }
        if ($type == "open")
        {
            $parent[$level -1] = & $current;
            if (!is_array($current) or (!in_array($tag, array_keys($current))))
            {
                $current[$tag] = $result;
                if ($attributes_data)
                  $current[$tag]['@'] = $attributes_data;
                $repeated_tag_index[$tag . '_' . $level] = 1;
                $current = & $current[$tag];
            }
            else
            {
                if (isset ($current[$tag][0]))
                {
                    if ($attributes_data)
                        $res['@'] = $attributes_data;

                    $current[$tag][$repeated_tag_index[$tag . '_' . $level]] = $res;
                    $repeated_tag_index[$tag . '_' . $level]++;
                }
                else
                {
                    if ($attributes_data)
                        $res['@'] = $attributes_data;

                    $current[$tag] = array (
                        $current[$tag],
                        $res
                    );
                    $repeated_tag_index[$tag . '_' . $level] = 2;
                    if (isset ($current[$tag]['@']))
                    {
                        $current[$tag]['0_attr'] = $current[$tag]['@'];
                        unset ($current[$tag]['@']);
                    }
                }
                $last_item_index = $repeated_tag_index[$tag . '_' . $level] - 1;
                $current = & $current[$tag][$last_item_index];
            }
        }
        elseif ($type == "complete")
        {
            if (!isset ($current[$tag]))
            {
                $current[$tag] = $result;
                $repeated_tag_index[$tag . '_' . $level] = 1;
                if ($priority == 'tag' and $attributes_data)
                {
                    unset($current[$tag]);
                    $current[$tag][0]['#'] = $result;
                    $current[$tag][0]['@'] = $attributes_data;
                }
            }
            else
            {
                if (isset ($current[$tag][0]) and is_array($current[$tag]))
                {
                    if ($priority == 'tag' and $get_attributes and $attributes_data)
                    {
                        $current[$tag][$repeated_tag_index[$tag . '_' . $level]]['#'] = $result;
                        $current[$tag][$repeated_tag_index[$tag . '_' . $level]]['@'] = $attributes_data;
                    }
                    else
                    {
                        $current[$tag][$repeated_tag_index[$tag . '_' . $level]] = $result;
                    }
                    $repeated_tag_index[$tag . '_' . $level]++;
                }
                else
                {
                    $current[$tag] = array (
                        $current[$tag],
                        $result
                    );
                    $repeated_tag_index[$tag . '_' . $level] = 1;
                    if ($priority == 'tag' and $get_attributes)
                    {
                        if (isset ($current[$tag]['@']))
                        {
                            $current[$tag]['0_attr'] = $current[$tag]['@'];
                            unset ($current[$tag]['@']);
                        }
                        if ($attributes_data)
                        {
                            $current[$tag][$repeated_tag_index[$tag . '_' . $level]]['@'] = $attributes_data;
                        }
                    }
                    $repeated_tag_index[$tag . '_' . $level]++; //0 and 1 index is already taken
                }
            }
        }
        elseif ($type == 'close')
        {
            $current = & $parent[$level -1];
        }
    }
    return ($xml_array);
}

